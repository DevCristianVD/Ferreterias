    private void configurarTabla() {
        String[] encabezadosTabla = new String[]{"ID", "Categor√≠a"};
        modeloTabla.setColumnIdentifiers(encabezadosTabla);
        TablaCategorias.setModel(modeloTabla);
    }
    
    public void llenar_tabla() {
        modeloTabla.setRowCount(0); 

        try {
            CategoriaDAO dao = new CategoriaDAO();
            List<Categoria> lista = dao.obtenerCategorias();

            for (Categoria c : lista) {
                modeloTabla.addRow(new Object[]{ c.getIdCategoria(), c.getNombre() });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar: " + e.getMessage());
        }
    }

