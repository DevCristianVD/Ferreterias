    private void BotonActualizarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarCActionPerformed
        int fila = TablaMarcas.getSelectedRow();
        if (fila >= 0) {
            int id = (int) TablaMarcas.getValueAt(fila, 0); // Asumiendo columna 0 es ID
            
            javax.swing.JFrame frame = (javax.swing.JFrame) javax.swing.SwingUtilities.getWindowAncestor(this);
            // Pasamos el ID seleccionado
            Dialogs.ActualizarMarca dialogo = new Dialogs.ActualizarMarca(frame, true, id);
            dialogo.setVisible(true);
            llenar_tabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una marca.");
        }
    }//GEN-LAST:event_BotonActualizarCActionPerformed


