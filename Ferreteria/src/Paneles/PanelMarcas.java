    public void llenar_tabla() {
        // Limpiamos la tabla antes de llenarla
        // Asegúrate de que 'TablaMarcas.getModel()' sea un DefaultTableModel
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) TablaMarcas.getModel();
        modelo.setRowCount(0); 

        try {
            DAO.MarcaDAO dao = new DAO.MarcaDAO();
            java.util.List<Clases.Marca> lista = dao.obtenerMarcas();

            for (Clases.Marca m : lista) {
                // Agregamos la fila: ID y Nombre
                modelo.addRow(new Object[]{
                    m.getIdMarca(), 
                    m.getNombre()
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al cargar marcas: " + e.getMessage());
        }
    }

