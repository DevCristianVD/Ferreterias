    private void configurarTabla() {
        // Columnas principales para la vista rápida
        String[] columnas = {"ID", "Nombre Completo", "Teléfono", "Horario", "Usuario"};
        // Hacemos que la tabla no sea editable
        DefaultTableModel modelo = new DefaultTableModel(null, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tablaEmpleados.setModel(modelo);
    }

    // Método público para poder recargar desde fuera
    public void cargarDatos() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) tablaEmpleados.getModel();
            modelo.setRowCount(0); // Limpiar tabla
            
            EmpleadoDAO dao = new EmpleadoDAO();
            List<Empleado> lista = dao.obtenerEmpleados(); // Usamos el método que ya tenías
            
            for (Empleado e : lista) {
                modelo.addRow(new Object[]{
                    e.getIdEmpleado(),
                    e.getNombreCompleto(), // Usamos el atributo calculado
                    e.getTelefono(),
                    e.getHorario(),
                    // Si tu método obtenerEmpleados no trae usuario, quita esta línea o modifica el DAO
                    // e.getUsuario() 
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar empleados: " + e.getMessage());
        }
    }

