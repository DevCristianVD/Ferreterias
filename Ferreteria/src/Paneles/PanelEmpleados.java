    private void BotonActualizarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarCActionPerformed
        // 1. Verificar fila seleccionada
        // IMPORTANTE: Cambia 'tablaEmpleados' por el nombre real de tu variable de tabla (ej. jTable1)
        int fila = tablaEmpleados.getSelectedRow();
        
        if (fila >= 0) {
            // 2. Obtener ID (asumiendo que está en la columna 0)
            int id = (int) tablaEmpleados.getValueAt(fila, 0);
            
            // 3. Obtener frame padre
            javax.swing.JFrame frame = (javax.swing.JFrame) javax.swing.SwingUtilities.getWindowAncestor(this);
            
            // 4. Abrir diálogo en MODO EDICIÓN (pasando el ID)
            Dialogs.actualizarEmpleados dialogo = new Dialogs.actualizarEmpleados(frame, true, id);
            dialogo.setVisible(true);
            
            // 5. Recargar tabla al volver
            cargarDatos(); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un empleado de la tabla.");
        }
    }//GEN-LAST:event_BotonActualizarCActionPerformed

